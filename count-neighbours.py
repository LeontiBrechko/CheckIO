def count_neighbours(grid, row, col):
    neighbours = 0
    for r in range(row - 1,row + 2):
        for c in range(col - 1,col + 2):
            if 0 <= r < len(grid) and 0 <= c < len(grid[r]):
                if grid[r][c]:
                    neighbours += 1
    if grid[row][col]:
        neighbours -= 1
    return neighbours




